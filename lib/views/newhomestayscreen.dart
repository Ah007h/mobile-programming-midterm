  void insertHomeStay() {
    String prname = _prnameEditingController.text;
    String prdesc = _prdescEditingController.text;
    String prprice = _prpriceEditingController.text;
    String sleeps = _prdelEditingController.text;
    String available = _prqtyEditingController.text;
    String state = _prstateEditingController.text;
    String local = _prlocalEditingController.text;
    String base64Image = base64Encode(_image!.readAsBytesSync());
    String base64Image1 = base64Encode(_image1!.readAsBytesSync());
    String base64Image2 = base64Encode(_image2!.readAsBytesSync());

    http.post(Uri.parse("${ServerConfig.SERVER}php/insert_homestay.php"),
        body: {
          "userid": widget.user.id,
          "prname": prname,
          "prdesc": prdesc,
          "prprice": prprice,
          "sleeps": sleeps,
          "available": available,
          "state": state,
          "local": local,
          "lat": _lat,
          "lon": _lng,
          "image": base64Image,
          "image1": base64Image1,
          "image2": base64Image2
        }).then((response) {
      var data = jsonDecode(response.body);
      if (response.statusCode == 200 && data['status'] == "success") {
        Fluttertoast.showToast(
            msg: "Success",
            toastLength: Toast.LENGTH_SHORT,
            gravity: ToastGravity.BOTTOM,
            timeInSecForIosWeb: 1,
            fontSize: 14.0);
        Navigator.of(context).pop();
        return;
      } else {
        Fluttertoast.showToast(
            msg: "Failed",
            toastLength: Toast.LENGTH_SHORT,
            gravity: ToastGravity.BOTTOM,
            timeInSecForIosWeb: 1,
            fontSize: 14.0);
        return;
      }
    });
  }
